package org.polymodel.util;

public class PolymodelException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 3513151925297217066L;

	public PolymodelException() {
		super();
	}

	public PolymodelException(String message, Throwable cause) {
		super(message, cause);
	}

	public PolymodelException(String message) {
		super(message);
	}


}
